package main

import "fmt"

func main() {
	var x, y int
	fmt.Println("Enter the 1st value:- ")
	fmt.Scanf("%d",&x)
	fmt.Println("Enter the 2nd value:- ")
	fmt.Scanf("%d",&y)
	fmt.Println("Addition:- ",x + y)
	fmt.Println("Subtraction:- ",x - y)
	fmt.Println("Multiplication:- ",x * y)
	fmt.Println("Division:- ",x / y)
}
